                                <h1>Vragen over bladmuziek</h1>

                                <h2>Hebben jullie bladmuziek van ...?</h2>

                                <p>De collectie bladmuziek bevat een schat aan Nederlands repertoire vanaf ca. 1870. Het TIN bezit veel losse, orginele, soms zelfs handgeschreven bladmuzieknotaties (Louis Davids, Harry Bannink). En daarnaast is een grote collectie internationale songbooks van musicals. In de database kunt u bij bladmuziek kijken of we een bepaald nummer in de collectie hebben. Het beste resultaat krijgt u wanneer u op titel van een lied of musical zoekt. Niet bij alle bladmuziek is namelijk de naam van de uitvoerende ingevoerd, omdat veel liedjes meerdere uitvoerenden kennen.U kunt natuurlijk wel zoeken op persoon (bijv. de componist of tekstschrijver). Tik hier eerst de achternaam in, en selecteer vervolgens met lijst personen de juiste persoon. Nb: Er is maar weinig actueel Nederlands cabaretrepertoire waarvan de muziek in druk verschijnt. Alleen nummers die tot de hitlijsten zijn doorgedrongen worden nog wel eens in bundels opgenomen.</p>
